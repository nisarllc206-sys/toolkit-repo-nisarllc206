document.addEventListener('DOMContentLoaded', () => {
  const form = document.getElementById('generate-form');
  const tempSlider = document.getElementById('gen-temp');
  const tempVal = document.getElementById('gen-temp-val');

  if (tempSlider && tempVal) {
    tempSlider.addEventListener('input', () => {
      tempVal.textContent = tempSlider.value;
    });
  }

  if (!form) return;

  form.addEventListener('submit', async (e) => {
    e.preventDefault();

    const prompt = document.getElementById('gen-prompt').value;
    const type = document.getElementById('gen-type').value;
    const maxTokens = parseInt(document.getElementById('gen-tokens').value) || 2000;
    const temperature = parseFloat(document.getElementById('gen-temp')?.value) || 0.7;

    const btn = document.getElementById('gen-btn');
    const origText = btn.innerHTML;
    btn.innerHTML = '<span class="material-icons spin">refresh</span> Generating...';
    btn.disabled = true;

    const resultCard = document.getElementById('gen-result-card');
    const contentEl = document.getElementById('generated-content');
    const seoEl = document.getElementById('seo-metadata');

    if (seoEl) seoEl.style.display = 'none';

    // Demo mode
    const isDemoMode = localStorage.getItem('auth_token') === 'demo-token';
    if (isDemoMode) {
      await simulateGeneration(prompt, type, contentEl, resultCard);
      btn.innerHTML = origText;
      btn.disabled = false;
      return;
    }

    try {
      const data = await window.api.post('/content/generate', {
        prompt,
        type,
        options: { maxTokens, temperature },
      });

      if (contentEl) contentEl.textContent = data.content;
      if (resultCard) resultCard.style.display = 'block';

      // Store generated content for SEO optimization
      window._lastGeneratedContent = data.content;

      showToast('Content generated successfully!', 'success');
    } catch (err) {
      showToast(err.message || 'Generation failed', 'error');
      // Fall back to demo mode
      await simulateGeneration(prompt, type, contentEl, resultCard);
    } finally {
      btn.innerHTML = origText;
      btn.disabled = false;
    }
  });
});

